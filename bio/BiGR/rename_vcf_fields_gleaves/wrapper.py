from typing import List

def generate_sed_params(sequence: str, replacement: str) -> List[str]:
    """Return sed substitution parameter"""
    return [
        f"s|INFO=<ID={sequence.replace("|", "\|")},|INFO=<ID={replacement.replace("|", "\|")},|g",
        f"s/\(\t|;\)\?{sequence.replace("/", "\/")}=/{replacement.replace("/", "\/")}=/g"
    ]


def search_header(header_list: List[str], word: str) -> bool:
    """Return True if a given word is a key in headers"""
    for header in header_list:
        if f"INFO=<ID={word}," in header:
            return True
    return False


